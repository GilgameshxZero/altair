#include <bits/stdc++.h>

using LL = long long;
using LD = long double;
using namespace std;

#define RF(x, from, to) \
	for (LL x(from), _to(to), _delta{x < _to ? 1LL : -1LL}; x != _to; x += _delta)

int main(int, char const *[]) {
	ios_base::sync_with_stdio(false);
	cin.tie(nullptr);

	LL N, B;
	cin >> N >> B;

	LL n{N}, ans{0};
	while (n > 0) {
		n = (n - B + B) / (B + 1);
		ans++;
	}
	cout << ans;
	return 0;
}
